-- 직원을 First_Name을 기준으로 오름차순 정렬해서 조회
SELECT *
FROM EMPLOYEES
ORDER BY FIRST_NAME ASC;

SELECT *
FROM EMPLOYEES
ORDER BY FIRST_NAME DESC;

-- SELECT EMPLOYEE_ID, FIRST_NAME, LAST_NAME, EMAIL, PHONE_NUMBER, HIRE_DATE, SALARY
SELECT 
    EMPLOYEE_ID
    , FIRST_NAME || ' '  || LAST_NAME AS NAME -- 수직바 2개 (||)를 사용해서 문자열 결합
    , EMAIL
    , PHONE_NUMBER
    , HIRE_DATE
    , SALARY
FROM EMPLOYEES
