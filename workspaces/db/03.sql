-- 급여가 15000 이상인 직원 조회
SELECT *
FROM EMPLOYEES
WHERE SALARY >= 15000;

-- 입사년도가 2005년인 직원 조회
SELECT *
FROM EMPLOYEES
WHERE HIRE_DATE >= '2005-01-01' AND HIRE_DATE <= '2005-12-31';

SELECT *
FROM EMPLOYEES
WHERE HIRE_DATE BETWEEN '2005-01-01' AND '2005-12-31';

