FROM ORDERS
WHERE ORDERDATE >= '2020-07-04' AND ORDERDATE <= '2020-07-07';

SELECT *
FROM ORDERS
WHERE ORDERDATE BETWEEN '2020-07-04' AND '2020-07-07';

-- 2020년 7월 4일~7월 7일 사이에 주문 받은 도서를 제외한 도서의 주문번호
SELECT *
FROM ORDERS
WHERE ORDERDATE < '2020-07-04' OR ORDERDATE > '2020-07-07';

SELECT *
FROM ORDERS
