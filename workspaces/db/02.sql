SELECT * FROM CUSTOMER; -- 이 조회를 통해 박지성 고객의 CUSTID가 1인 것을 확인

SELECT 
    '박지성' AS "고객명" -- 리터럴을 조회 결과에 포함할 수 있습니다.
    , SUM(SALEPRICE) AS "총구매액"
    , COUNT(*) AS "수량"
